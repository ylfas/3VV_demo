def computer_angle(v1, v2):
    dot_mut = np.multiply(v1, v2).sum()
    lenth1 = np.sqrt(v1 ** 2).sum()
    lenth2 = np.sqrt(v2 ** 2).sum()
    angle = dot_mut/(lenth1*lenth2)
    return angle


class FocalLoss2(nn.Module):
    def __init__(self, alpha=1, gamma=2, logits=False, reduce=True):
        super(FocalLoss2, self).__init__()
        self.alpha = alpha
        self.gamma = gamma
        self.logits = logits
        self.reduce = reduce

    def forward(self, inputs, targets):
        if self.logits:
            BCE_loss = F.cross_entropy_with_logits(inputs, targets, reduce=False)
        else:
            BCE_loss = F.cross_entropy(inputs, targets, reduce=False)
        pt = torch.exp(-BCE_loss)
        F_loss = self.alpha * (1-pt)**self.gamma * BCE_loss

        if self.reduce:
            return torch.mean(F_loss)
        else:
            return F_loss
